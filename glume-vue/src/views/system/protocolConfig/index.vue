<template>
  <layout-container>
    <el-card class="box-card min-h8">
      <el-row class="mb8">
        <quill-editor class="editor" v-model="content" ref="myQuillEditor" :options="editorOption"
                      @focus="onEditorFocus($event)"
                      @blur="onEditorBlur($event)"
                      @change="onEditorChange($event)"/>
      </el-row>
    </el-card>
  </layout-container>
</template>

<script>
  import 'quill/dist/quill.core.css'
  import 'quill/dist/quill.snow.css'
  import 'quill/dist/quill.bubble.css'
  import { quillEditor } from 'vue-quill-editor'
  import { getProtocolConfig,amendProtocolConfig } from '@/api/protocolConfig'
  import LayoutContainer from '@/components/LayoutContainer/LayoutContainer'

  // 工具栏配置
  const toolbarOptions = [
    ["bold", "italic", "underline", "strike"], // 加粗 斜体 下划线 删除线 -----['bold', 'italic', 'underline', 'strike']
    ["blockquote", "code-block"], // 引用  代码块-----['blockquote', 'code-block']
    [{ header: 1 }, { header: 2 }], // 1、2 级标题-----[{ header: 1 }, { header: 2 }]
    [{ list: "ordered" }, { list: "bullet" }], // 有序、无序列表-----[{ list: 'ordered' }, { list: 'bullet' }]
    [{ script: "sub" }, { script: "super" }], // 上标/下标-----[{ script: 'sub' }, { script: 'super' }]
    [{ indent: "-1" }, { indent: "+1" }], // 缩进-----[{ indent: '-1' }, { indent: '+1' }]
    [{ direction: "rtl" }], // 文本方向-----[{'direction': 'rtl'}]
    [{ size: ["small", false, "large", "huge"] }], // 字体大小-----[{ size: ['small', false, 'large', 'huge'] }]
    [{ header: [1, 2, 3, 4, 5, 6, false] }], // 标题-----[{ header: [1, 2, 3, 4, 5, 6, false] }]
    [{ color: [] }, { background: [] }], // 字体颜色、字体背景颜色-----[{ color: [] }, { background: [] }]
    [{ font: [] }], // 字体种类-----[{ font: [] }]
    [{ align: [] }], // 对齐方式-----[{ align: [] }]
    ["clean"], // 清除文本格式-----['clean']
    ["image", "video"] // 链接、图片、视频-----['link', 'image', 'video']
  ];
  export default {
    name: 'index',
    components: { quillEditor,LayoutContainer },
    data() {
      return {
        content: "",
        editorOption: {
          //  富文本编辑器配置
          modules: {
            toolbar: toolbarOptions
          },
          theme: "snow",
          placeholder: "请输入正文"
        }
      }
    },
    computed: {
      //当前富文本实例
      editor() {
        return this.$refs.myQuillEditor.quill;
      }
    },
    mounted() {
      this.initProtocolConfig();
    },
    methods: {
      initProtocolConfig() {
        getProtocolConfig().then(response => {
          this.content = response.data.content;
        });
      },
      //获得焦点事件
      onEditorFocus(e) {
        console.log(1)
      },
      //失去焦点事件
      onEditorBlur(e) {
        console.log(2)
        this.handleSave(this.content)
      },
      //内容改变事件
      onEditorChange(e) {
        console.log(3)
      },
      handleSave(e) {
        amendProtocolConfig({content: e}).then(response => {
          console.log(response)
        })
      }
    }
  }
</script>

